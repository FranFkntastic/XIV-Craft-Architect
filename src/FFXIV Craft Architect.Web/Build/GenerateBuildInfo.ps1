param(
    [Parameter(Mandatory = $true)]
    [string] $ProjectDirectory,

    [Parameter(Mandatory = $true)]
    [string] $OutputPath,

    [string] $BranchName = ''
)

Set-StrictMode -Version 2.0
$ErrorActionPreference = 'Stop'

function Invoke-Git {
    param(
        [Parameter(Mandatory = $true)]
        [string] $RepositoryRoot,

        [Parameter(Mandatory = $true)]
        [string[]] $Arguments
    )

    try {
        $value = & git -C $RepositoryRoot @Arguments 2>$null
        if ($LASTEXITCODE -ne 0) {
            return ''
        }

        return ($value -join "`n").Trim()
    }
    catch {
        return ''
    }
}

function Escape-CSharpString {
    param([string] $Value)

    if ($null -eq $Value) {
        return ''
    }

    return $Value.Replace('\', '\\').Replace('"', '\"')
}

$repositoryRoot = Invoke-Git -RepositoryRoot $ProjectDirectory -Arguments @('rev-parse', '--show-toplevel')
if ([string]::IsNullOrWhiteSpace($repositoryRoot)) {
    $repositoryRoot = $ProjectDirectory
}

$branchName = $BranchName
if ([string]::IsNullOrWhiteSpace($branchName)) {
    $branchName = $env:GITHUB_REF_NAME
}

if ([string]::IsNullOrWhiteSpace($branchName)) {
    $branchName = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @('branch', '--show-current')
}

if ([string]::IsNullOrWhiteSpace($branchName)) {
    $branchName = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @('rev-parse', '--abbrev-ref', 'HEAD')
}

if ([string]::IsNullOrWhiteSpace($branchName)) {
    $branchName = 'unknown'
}

$commitSha = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @('rev-parse', '--short', 'HEAD')
if ([string]::IsNullOrWhiteSpace($commitSha)) {
    $commitSha = 'unknown'
}

$commitDate = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @('show', '-s', '--format=%cd', '--date=format:%Y-%m-%d', 'HEAD')
if ([string]::IsNullOrWhiteSpace($commitDate)) {
    $commitDate = [DateTime]::UtcNow.ToString('yyyy-MM-dd', [Globalization.CultureInfo]::InvariantCulture)
}

# Daily ordinal is equivalent to: git rev-list --count --since=<date> --until=<date> HEAD
$dailyBuildNumberText = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @(
    'rev-list',
    '--count',
    "--since=$commitDate 00:00:00",
    "--until=$commitDate 23:59:59",
    'HEAD')

$dailyBuildNumber = 1
if (-not [int]::TryParse($dailyBuildNumberText, [ref] $dailyBuildNumber) -or $dailyBuildNumber -lt 1) {
    $dailyBuildNumber = 1
}

$dirtyStatus = Invoke-Git -RepositoryRoot $repositoryRoot -Arguments @('status', '--porcelain', '--untracked-files=no')
$isDirty = -not [string]::IsNullOrWhiteSpace($dirtyStatus)
$isDirtyLiteral = if ($isDirty) { 'true' } else { 'false' }
$buildDate = $commitDate.Replace('-', '.')
$buildVersion = "$buildDate.$dailyBuildNumber"
$generatedAtUtc = [DateTime]::UtcNow.ToString('o', [Globalization.CultureInfo]::InvariantCulture)

$outputDirectory = Split-Path -Path $OutputPath -Parent
if (-not [string]::IsNullOrWhiteSpace($outputDirectory)) {
    New-Item -ItemType Directory -Path $outputDirectory -Force | Out-Null
}

$escapedBuildDate = Escape-CSharpString $buildDate
$escapedBuildVersion = Escape-CSharpString $buildVersion
$escapedBranchName = Escape-CSharpString $branchName
$escapedCommitSha = Escape-CSharpString $commitSha
$escapedGeneratedAtUtc = Escape-CSharpString $generatedAtUtc

@"
// <auto-generated />
namespace FFXIV_Craft_Architect.Web;

internal static class WebBuildInfo
{
    public const string BuildDate = "$escapedBuildDate";
    public const int DailyBuildNumber = $dailyBuildNumber;
    public const string BuildVersion = "$escapedBuildVersion";
    public const string BranchName = "$escapedBranchName";
    public const string CommitSha = "$escapedCommitSha";
    public const bool IsDirty = $isDirtyLiteral;
    public const string GeneratedAtUtc = "$escapedGeneratedAtUtc";
}
"@ | Set-Content -Path $OutputPath -Encoding UTF8
